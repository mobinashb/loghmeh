package ie.projects.phase7.service.user.request;

public class SignupRequest {
    String firstName;
    String lastName;
    String email;
    String password;

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }
}
